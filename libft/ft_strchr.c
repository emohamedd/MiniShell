#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;
	char	x;

	i = 0;
	x = (char)c;
	while (i <= ft_strlen(s))
	{
		if (s[i] == x)
			return ((char *)&s[i]);
		i++;
	}
	return (NULL);
}
