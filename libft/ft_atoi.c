#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	stock;
	int	sign;

	i = 0;
	stock = 0;
	sign = 1;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] && (str[i] >= '0' && str[i] <= '9'))
	{
		stock = (stock * 10) + (str[i] - '0');
		i++;
	}
	return (sign * stock);
}
